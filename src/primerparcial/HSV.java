
package primerparcial;

public class HSV {
    protected double H;
    protected double S;
    protected double V;

    public HSV(double H, double S, double V) {
        this.H = H;
        this.S = S;
        this.V = V;
    }
    

    public double getH() {
        return H;
    }

    public void setH(double H) {
        this.H = H;
    }

    public double getS() {
        return S;
    }

    public void setS(double S) {
        this.S = S;
    }

    public double getV() {
        return V;
    }

    public void setV(double V) {
        this.V = V;
    }
   
    
    
    
}
